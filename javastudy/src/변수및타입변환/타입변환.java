package 변수및타입변환;

public class 타입변환 {
	public static void main(String[] args) 
	{
   //타입변환
   int a = 10;
   double b = (double)a;   //타입 변환 원하는 타입앞에 써서 변환 (타입을 변환할)변수
   System.out.println(b); //10
   
   
   
   //더하기
   int x = 1;
   int y= 2;
   double result = (double)x + y;
   System.out.println(result);
	}
}
